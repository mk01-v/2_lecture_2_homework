# список объектов с метками или ключами. Так удобнее.
d = {'os': 'windows', 'os_version': '8.1', 'browser':'firefox', 'browser_version':'35.0.1'}
print(d['os'])
print(d['browser'])

# вложенные словари.
k = {'os': {'type':'windows', 'version':'8.1'}, 'browser': {'type':'firefox', 'version':'35.0.1'}}
print(k['browser']['version'])





























